package com.example.chapter03.practice03;

/**
 * 【問題03：スマホのバッテリーを正しく充電しよう（コンストラクタでガード＋自作メソッド）】
 *
 * これは、スマホのバッテリー残量（0〜100%）を管理するプログラムです。
 *
 * ▼現在の状態（このプログラムをそのまま実行した場合）
 * コンパイルエラー（charge メソッドが未定義）
 *
 * ▼期待される出力結果
 * バッテリー: 30%
 * バッテリー: 100%
 *
 * ▼指示
 * ・Battery のコンストラクタでガード処理を実装してください。
 *   初期残量（int型の引数）が 0 未満なら 0、100 を超えるなら 100 に直してから保持します。
 * ・あなた自身で Battery に charge メソッド（充電をチャージするメソッド）を 1 から作成してください。
 *   メソッドの仕様：現在の残量に 引数で受け取った int型のpercent を加算し、結果を 0〜100 に収める（100 超えは 100）。
 *                percent は 0 以上を想定。0 未満が来たら何もしなくてOKです。
 * ・あなた自身で Battery に label メソッド（充電が何%か表示するメソッド）を 1 から作成してください。
 *   メソッドの仕様：現在の残量を「バッテリー: X%」の形式で表示します。
 */
class Main {
  public static void main(String[] args) {
    // ケース1：初期値 -10 → コンストラクタのガードで 0 に修正
    Battery b1 = new Battery(-10);
    b1.charge(30); // 0 + 30 = 30 → 30%
    System.out.println(b1.label()); // 期待出される力結果： バッテリー: 30%

    // ケース2：初期値 95 → charge(10) で 105 だが上限は 100
    Battery b2 = new Battery(95);
    b2.charge(10); // 95 + 10 = 105 -> 100 に丸め込み
    System.out.println(b2.label()); // 期待出される力結果： バッテリー: 100%
  }
}