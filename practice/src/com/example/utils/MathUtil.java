package com.example.utils;

public class MathUtil { // static フィールド（クラスフィールド）
    // ─────────────────────────────
// ・所属：クラスに属する共有状態
// ・生成タイミング：このクラスが「初めて能動的に使用された時」に
//   クラス初期化が行われ、その際に生成・初期化される
// ・new MathUtil() を一度も呼ばなくても参照されうる
// ・インスタンスとは無関係に 1 つだけ存在する
// ・全ての参照者は同一の実体を共有するため、
//   再代入は「全体の前提条件」を書き換える行為になる。

    public static int money = 100;
    // インスタンスフィールド
// ─────────────────────────────
// ・所属：各インスタンスに属する状態
// ・生成タイミング：new によってインスタンスが生成された時
// ・インスタンスごとに独立した実体を持つ
// ・他のインスタンスの状態には一切影響しない
    public int money1 = 100;

    // コンストラクタ
// ─────────────────────────────
// ・インスタンス生成時に呼び出される
// ・static フィールドの生成や存在とは無関係
    public MathUtil(int i, int i1) {
        System.out.println(i + i1);
    }

    // static メソッド
// ─────────────────────────────
// ・クラスに属する振る舞い
// ・インスタンス状態を前提としない処理のみを書く
// ・this やインスタンスフィールドには直接アクセスできない
// ・インスタンス参照（this）が存在しない文脈で、インスタンスメソッドを呼び出そうとする行為は、論理以前に成立しない。
    public static int add(int a, int b) {
        return a + b;
    }

    public static int test() {
        MathUtil testInstance = new MathUtil(1, 1);
        return money + testInstance.money1;
    }

}
