package com.example.chapter02;

/*
  【問題07：コーヒー注文シミュレーション】
  あなたはコーヒーショップで注文をしています。
  do-while文を使って「コーヒーを1杯注文する」動作を繰り返してください。
  ただし、注文した合計金額が 1000円 を超えたら注文をやめ、
  「→ お会計に進みます」と表示してください。

  ▼期待される出力結果
  コーヒーを注文しました（1杯目）
  コーヒーを注文しました（2杯目）
  コーヒーを注文しました（3杯目）
  コーヒーを注文しました（4杯目）
  コーヒーを注文しました（5杯目）
  → お会計に進みます

  ▼前提条件
  ・1杯のコーヒーは200円とします。

  ▼指示
  ・do-while文を使って必ず1杯は注文されるようにしてください。
  ・合計金額が1000円以上になったらループを終了してください。
  ・出力形式はサンプル実行例を参考にしてください。

  ▼修正ポイント
  ・do-while文の利用
      誤りがちな例: while だけで書いてしまい、初回の注文を逃す。
      修正: do { ... } while (条件式); で必ず1回注文させる。

  ・合計金額の判定
      誤りがちな例: while (total <= 1000) としてしまうと、1000円ちょうどのときにもう1杯余計に注文してしまう。
      修正: while (total < 1000) として、1000円以上になったら終了させる。

  ・杯数のカウント
      誤りがちな例: count のインクリメントを忘れる。
      修正: count++ を注文のたびに実行し、「〇杯目」が適切に出力されるようにする。
*/
public class Kadai07 {

  public static void main(String[] args) {
    int price = 200;  // 1杯あたりの価格
    int total = 0;    // 合計金額
    int count = 0;    // 注文した杯数

    // ここに do-while文を書いて完成させる
    do {
      count++;                // 注文した杯数をカウント
      total += price;         // 合計金額を加算
      System.out.println("コーヒーを注文しました（" + count + "杯目）");
    } while (total < 1000);   // 1000円以上になったら終了（つまり、1000円未満の場合は繰り返す）

    System.out.println("→ お会計に進みます");
  }
}